# 入力
n = int(input())  # 数列の長さ
s = input()       # 数列（文字列として取得）

# ステップ 1: A の計算
# 各桁ごとに (i + 1) * S[i] を計算し、累積和を取る
a = [(i + 1) * int(s[i]) for i in range(n)]
for i in range(1, n):
    a[i] += a[i - 1]  # 累積和を計算

# ステップ 2: 繰り上がりを処理しながら答えを構築
i = 0
c = 0
ans = []  # 答えを格納するリスト
while i < n or c > 0:  # すべての桁を処理し、繰り上がりがなくなるまでループ
    if i < n:
        c += a[n - 1 - i]  # A の値を繰り上がりに加算
    ans.append(c % 10)     # 現在の桁の値を記録
    c //= 10               # 繰り上がりの計算
    i += 1

# 答えを出力
print(*ans[::-1], sep="")  # 桁を反転して出力
