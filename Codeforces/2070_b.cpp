int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int t;
  cin >> t;
  while (t--) {
    ll m, x, k;
    cin >> m >> x >> k;
    string s;
    cin >> s;

    // First cycle: simulate starting from initial position x.
    ll d = 0;      // cumulative displacement
    ll first = -1; // time (in seconds) when robot first reaches 0
    for (ll i = 0; i < m; i++) {
      if (s[i] == 'L')
        d -= 1;
      else
        d += 1;
      if (x + d == 0) {
        first = i + 1; // +1 because i is 0-indexed (i+1 seconds elapsed)
        break;
      }
    }

    // If never reached 0 or it reaches 0 after k seconds, answer is 0.
    if (first == -1 || first > k) {
      cout << 0 << "\n";
      continue;
    }

    // Second cycle: simulate the sequence starting from 0.
    ll d2 = 0;
    ll cycle = -1; // time (in seconds) for one full cycle (starting from 0) to
                   // hit 0 again
    for (ll i = 0; i < m; i++) {
      if (s[i] == 'L')
        d2 -= 1;
      else
        d2 += 1;
      if (d2 == 0) {
        cycle = i + 1;
        break;
      }
    }

    // If the cycle never returns to 0, only the first hit is counted.
    if (cycle == -1) {
      cout << 1 << "\n";
    } else {
      // Additional full cycles that can be executed in the remaining (k -
      // first) seconds.
      ll additional = (k - first) / cycle;
      cout << 1 + additional << "\n";
    }
  }
  return 0;
}